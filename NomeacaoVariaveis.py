# Declarando variáveis em uma mesma linha

um, dois, tres = 1, 2, 3

print(um, dois, tres)

# Atribuir um valor a diversas variáveis

# Vamos tentar atribuir o valor 100 em inumeras variáveis

morango = uva = melancia = 100

print(morango, uva, melancia)

# nomear / criar variáveis usando listas

lista_carros =  ['volkswagen', 'audi', 'tesla']

# Agora posso criar um conjunto de variáveis na mesma linha que irão
# receber como valor, os valores presentes na lista de carros

marca_01, marca_02, marca_03 = lista_carros

print(marca_01, marca_02, marca_03)

# Combinação de Variáveis

nome = "José"

sobrenome = "Cesar"

nome_completo = nome + " " + sobrenome

print(nome_completo)

